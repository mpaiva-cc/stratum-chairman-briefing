#!/usr/bin/env node
// CSS deduplication: promotes rules duplicated across ≥THRESHOLD pages to
// /assets/styles.css and strips the byte-identical copies from each page's
// inline <style> blocks.
//
// Safety:
//   - Hashes by exact byte match (after whitespace normalization). Pages
//     with subtly-different variant rules KEEP their inline override.
//   - Stylesheet <link> is injected BEFORE inline <style>, so per-page
//     inline rules still override the shared sheet.
//   - Idempotent: re-running detects the cache-buster marker and no-ops.
//
// Usage:
//   node scripts/css-consolidate.js              # dry run, prints summary
//   node scripts/css-consolidate.js --write      # apply
//   THRESHOLD env var sets duplication threshold (default 15)
'use strict';
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const ROOT = path.resolve(__dirname, '..');
const STYLES_CSS = path.join(ROOT, 'assets/styles.css');
const SKIP = new Set(['_site', '_includes', '_layouts', 'node_modules',
  'agent-outbox', '.git', '.claude', '.githooks', 'scripts', 'vendor']);

const WRITE = process.argv.includes('--write');
const THRESHOLD = parseInt(process.env.THRESHOLD || '15', 10);
const CACHE_VER = 'v=5';

// Sentinel comment in styles.css marking the promoted block. Lets us re-run
// safely: remove the old block, rebuild it from the current audit.
const PROMO_OPEN  = '/* ── PROMOTED-DUPS · auto-generated by scripts/css-consolidate.js ── DO NOT EDIT BY HAND ── */';
const PROMO_CLOSE = '/* ── /PROMOTED-DUPS ── */';

function walk(dir, out = []) {
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    if (SKIP.has(ent.name)) continue;
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) walk(p, out);
    else if (ent.isFile() && p.endsWith('.html')) out.push(p);
  }
  return out;
}

function extractStyleBlocks(html) {
  const out = [];
  const re = /<style\b[^>]*>([\s\S]*?)<\/style>/gi;
  let m;
  while ((m = re.exec(html)) !== null) out.push({ raw: m[0], inner: m[1], index: m.index, end: m.index + m[0].length });
  return out;
}

function splitRules(css) {
  const rules = [];
  let depth = 0, start = 0, inStr = null;
  for (let i = 0; i < css.length; i++) {
    const c = css[i];
    if (inStr) { if (c === '\\') { i++; continue; } if (c === inStr) inStr = null; continue; }
    if (c === '"' || c === "'") { inStr = c; continue; }
    if (c === '/' && css[i + 1] === '*') {
      const end = css.indexOf('*/', i + 2);
      if (end < 0) break;
      i = end + 1; continue;
    }
    if (c === '{') { depth++; continue; }
    if (c === '}') {
      depth--;
      if (depth === 0) {
        const full = css.slice(start, i + 1);
        const trimmed = full.replace(/^[\s;]+/, '');
        if (trimmed.includes('{')) {
          rules.push({ full: trimmed, start: start, end: i + 1 });
        }
        start = i + 1;
      }
    }
  }
  return rules;
}

function canonical(rule) {
  return rule.replace(/\s+/g, ' ').replace(/\s*([{}:;,])\s*/g, '$1').trim();
}
function hash(s) {
  return crypto.createHash('sha1').update(s).digest('hex').slice(0, 12);
}

function buildAudit(files) {
  const byHash = new Map();
  for (const f of files) {
    const html = fs.readFileSync(f, 'utf8');
    for (const b of extractStyleBlocks(html)) {
      for (const r of splitRules(b.inner)) {
        const h = hash(canonical(r.full));
        if (!byHash.has(h)) byHash.set(h, { canonical: canonical(r.full), sample: r.full, pages: new Set() });
        byHash.get(h).pages.add(f);
      }
    }
  }
  return byHash;
}

// Safety filter: never promote a rule whose selector has multiple distinct
// variants across the site. Two pages with `html,body{font-size:17px}` and
// `html,body{font-size:18px}` cannot both live in one shared stylesheet —
// whichever was written last wins for everyone, silently regressing the
// other. When that happens, we keep all variants inline.
function selectPromotions(audit) {
  const all = [...audit.entries()].map(([h, v]) => ({
    hash: h,
    canonical: v.canonical,
    sample: v.sample.trim(),
    pages: v.pages.size,
    bytes: v.sample.length,
    selector: v.canonical.split('{')[0].trim(),
  }));
  // Count distinct variants per selector across the WHOLE site (any page
  // count, not just ≥threshold), so we catch even small-N divergences.
  const variantsBySelector = new Map();
  for (const r of all) {
    if (!variantsBySelector.has(r.selector)) variantsBySelector.set(r.selector, 0);
    variantsBySelector.set(r.selector, variantsBySelector.get(r.selector) + 1);
  }
  return all
    .filter(r => r.pages >= THRESHOLD)
    .filter(r => variantsBySelector.get(r.selector) === 1)
    .sort((a, b) => (b.pages * b.bytes) - (a.pages * a.bytes));
}

function buildPromotedCss(promotions) {
  const lines = [PROMO_OPEN];
  lines.push(`/* ${promotions.length} rules promoted, each duplicated across ≥${THRESHOLD} pages. */`);
  lines.push('');
  for (const p of promotions) {
    lines.push(p.sample);
  }
  lines.push('');
  lines.push(PROMO_CLOSE);
  return lines.join('\n') + '\n';
}

function upsertPromotedCss(promotedCss) {
  let css;
  try { css = fs.readFileSync(STYLES_CSS, 'utf8'); }
  catch { css = ''; }
  // Strip any prior auto-generated block.
  const re = new RegExp(
    PROMO_OPEN.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') +
    '[\\s\\S]*?' +
    PROMO_CLOSE.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '\\n?',
    'g'
  );
  css = css.replace(re, '').trimEnd() + '\n\n' + promotedCss;
  return css;
}

function stripPromotedFromPage(html, promotedHashes) {
  const blocks = extractStyleBlocks(html);
  if (blocks.length === 0) return { html, removed: 0 };
  let removed = 0;
  // Process blocks back-to-front so offsets stay valid.
  for (let bi = blocks.length - 1; bi >= 0; bi--) {
    const b = blocks[bi];
    const rules = splitRules(b.inner);
    if (!rules.length) continue;
    // Determine which rules survive.
    let keptCss = '';
    let lastEnd = 0;
    for (const r of rules) {
      const h = hash(canonical(r.full));
      // Preserve inter-rule whitespace/comments by copying from b.inner.
      keptCss += b.inner.slice(lastEnd, r.start);
      if (!promotedHashes.has(h)) {
        keptCss += b.inner.slice(r.start, r.end);
      } else {
        removed += r.full.length;
      }
      lastEnd = r.end;
    }
    keptCss += b.inner.slice(lastEnd);
    // Collapse runs of blank lines and orphan comment-only blank space.
    keptCss = keptCss.replace(/\n[ \t]*\n[ \t]*\n+/g, '\n\n');
    // If the entire block is now empty/whitespace, remove it.
    const stripped = keptCss.replace(/\/\*[\s\S]*?\*\//g, '').trim();
    let replacement;
    if (stripped.length === 0) {
      replacement = '';
    } else {
      // Preserve the opening <style ...> attributes.
      const openTag = b.raw.match(/^<style\b[^>]*>/i)[0];
      replacement = openTag + keptCss + '</style>';
    }
    html = html.slice(0, b.index) + replacement + html.slice(b.end);
  }
  return { html, removed };
}

function ensureStylesheetLink(html) {
  // Already linked (Liquid-templated form) — bump version if older.
  if (/href="\{\{ ?'\/assets\/styles\.css\?v=/i.test(html)) {
    return html.replace(/href="\{\{ ?'\/assets\/styles\.css\?v=\d+' \| relative_url ?\}\}"/g, `href="{{ '/assets/styles.css?${CACHE_VER}' | relative_url }}"`);
  }
  // Already linked (raw absolute form, pre-baseurl-sweep) — upgrade.
  if (/href="\/assets\/styles\.css\?v=/i.test(html)) {
    return html.replace(/href="\/assets\/styles\.css\?v=\d+"/g, `href="{{ '/assets/styles.css?${CACHE_VER}' | relative_url }}"`);
  }
  // Also accept relative paths (legacy).
  if (/href="(?:\.\.\/)*assets\/styles\.css/i.test(html)) {
    return html.replace(/href="(?:\.\.\/)*assets\/styles\.css(?:\?v=\d+)?"/g, `href="{{ '/assets/styles.css?${CACHE_VER}' | relative_url }}"`);
  }
  // Inject before the first <style> tag in <head>, or right after <head>.
  const linkTag = `<link rel="stylesheet" href="{{ '/assets/styles.css?${CACHE_VER}' | relative_url }}">`;
  const styleIdx = html.search(/<style\b/i);
  const headOpenMatch = html.match(/<head\b[^>]*>/i);
  const headOpenEnd = headOpenMatch ? headOpenMatch.index + headOpenMatch[0].length : -1;
  if (styleIdx > 0 && styleIdx > headOpenEnd && headOpenEnd > 0) {
    return html.slice(0, styleIdx) + linkTag + '\n' + html.slice(styleIdx);
  }
  if (headOpenEnd > 0) {
    return html.slice(0, headOpenEnd) + '\n' + linkTag + html.slice(headOpenEnd);
  }
  return html; // no <head> — leave alone (rare; would be malformed page)
}

function stripDuplicatedNavToggleJs(html) {
  // Remove inline `<script>` blocks that contain the canonical site-nav
  // toggle logic (now lives in _includes/site-nav.html). Critical: do this
  // BLOCK-BY-BLOCK rather than with a global regex. A naive
  // /<script>[\s\S]*?\.sn-index-btn[\s\S]*?<\/script>/ will span across
  // unrelated <script>...</script> blocks and devour the page content
  // between them — pages with a small enhancement-guard script before
  // <body> get their entire body eaten.
  let removed = 0;
  const re = /<script\b[^>]*>([\s\S]*?)<\/script>\n?/gi;
  const out = html.replace(re, (full, body) => {
    if (body.includes('.sn-index-btn') && body.includes('.sn-hamburger') &&
        /document\.querySelector\s*\(\s*['"]\.sn-/.test(body)) {
      removed += full.length;
      return '';
    }
    return full;
  });
  return { html: out, removed };
}

function main() {
  const files = walk(ROOT);
  console.log(`Scanning ${files.length} files (threshold=${THRESHOLD})…\n`);

  const audit = buildAudit(files);
  const promotions = selectPromotions(audit);
  console.log(`Promotion candidates (≥${THRESHOLD}p): ${promotions.length} rules, ${(promotions.reduce((s,p)=>s+p.bytes,0)/1024).toFixed(1)} KB.`);

  const promotedHashes = new Set(promotions.map(p => p.hash));
  const promotedCss = buildPromotedCss(promotions);
  const newStylesCss = upsertPromotedCss(promotedCss);

  let totalRemoved = 0;
  let totalJsRemoved = 0;
  let pagesTouched = 0;
  let linksInjected = 0;

  for (const f of files) {
    let html = fs.readFileSync(f, 'utf8');
    const before = html;
    const beforeHasLink = /assets\/styles\.css\?v=/i.test(html);
    html = ensureStylesheetLink(html);
    if (!beforeHasLink && /assets\/styles\.css\?v=/i.test(html)) linksInjected++;
    const r1 = stripPromotedFromPage(html, promotedHashes);
    html = r1.html; totalRemoved += r1.removed;
    const r2 = stripDuplicatedNavToggleJs(html);
    html = r2.html; totalJsRemoved += r2.removed;
    if (html !== before) {
      pagesTouched++;
      if (WRITE) fs.writeFileSync(f, html);
    }
  }

  if (WRITE) {
    fs.writeFileSync(STYLES_CSS, newStylesCss);
    console.log(`\nWROTE:`);
    console.log(`  assets/styles.css  +${(promotedCss.length/1024).toFixed(1)} KB (auto-generated block)`);
  } else {
    console.log(`\nDRY RUN:`);
  }
  console.log(`  ${pagesTouched} pages touched`);
  console.log(`  ${(totalRemoved/1024).toFixed(1)} KB CSS removed from inline <style>`);
  console.log(`  ${(totalJsRemoved/1024).toFixed(1)} KB duplicated nav-toggle JS removed`);
  console.log(`  ${linksInjected} pages had <link rel="stylesheet"> injected`);
  if (!WRITE) console.log(`\nRe-run with --write to apply.`);
}

main();
